
public class Stop {
	 public int Latitude;
	 public String Name;
	 public String SubwayLine;
	 
	     //Setting Name of Stop
		 public void setName(String N)
		 {
			 Name = N;
		 }
		 
		//Getting Name of Stop
		 public String getName()
		 {
			 return Name;
		 }
		 
		 //Setting Name of Subway Line
		 public void setSubwayLine(String S)
		 {
			 SubwayLine = S;
		 }
		 
		//Getting Name of Subway Line
		 public String getSubwayLine()
		 {
			 return SubwayLine;
		 }
		 
		//Setting Latitude of Stop
		 public void setLatitude(int L)
		 {
			 Latitude = L;
		 }
		 
		//Getting Latitude of Subway Line
		 public int getLatitude()
		 {
			 return Latitude;
		 }
}
